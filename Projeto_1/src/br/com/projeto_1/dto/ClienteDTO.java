package br.com.projeto_1.dto;

/**
 *
 * @author Aluno
 */
public class ClienteDTO {
    private String nomeCli, logradouroCli, bairroCli, cidadeCli, estadoCli, cepCli, cpfCli, rgCli;
    private int idCli, numeroCli;

    public String getNomeCli() {
        return nomeCli;
    }

    public void setNomeCli(String nomeCli) {
        this.nomeCli = nomeCli;
    }

    public String getLogradouroCli() {
        return logradouroCli;
    }

    public void setLogradouroCli(String logradouroCli) {
        this.logradouroCli = logradouroCli;
    }

    public String getBairroCli() {
        return bairroCli;
    }

    public void setBairroCli(String bairroCli) {
        this.bairroCli = bairroCli;
    }

    public String getCidadeCli() {
        return cidadeCli;
    }

    public void setCidadeCli(String cidadeCli) {
        this.cidadeCli = cidadeCli;
    }

    public String getEstadoCli() {
        return estadoCli;
    }

    public void setEstadoCli(String estadoCli) {
        this.estadoCli = estadoCli;
    }

    public String getCepCli() {
        return cepCli;
    }

    public void setCepCli(String cepCli) {
        this.cepCli = cepCli;
    }

    public String getCpfCli() {
        return cpfCli;
    }

    public void setCpfCli(String cpfCli) {
        this.cpfCli = cpfCli;
    }

    public String getRgCli() {
        return rgCli;
    }

    public void setRgCli(String rgCli) {
        this.rgCli = rgCli;
    }

    public int getIdCli() {
        return idCli;
    }

    public void setIdCli(int idCli) {
        this.idCli = idCli;
    }

    public int getNumeroCli() {
        return numeroCli;
    }

    public void setNumeroCli(int numeroCli) {
        this.numeroCli = numeroCli;
    }
}
