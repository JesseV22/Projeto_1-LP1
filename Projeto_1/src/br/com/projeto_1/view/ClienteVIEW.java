    public void achaCidade() {
        String json;

        try {
            URL url = new URL("http://viacep.com.br/ws/" + Integer.parseInt(txtCep.getText()) + "/json");
            URLConnection urlConnection = url.openConnection();
            InputStream is = urlConnection.getInputStream();
            BufferedReader br = new BufferedReader(new InputStreamReader(is));

            StringBuilder jsonSb = new StringBuilder();

            br.lines().forEach(l -> jsonSb.append(l.trim()));
            json = jsonSb.toString();

            // JOptionPane.showMessageDialog(null, json);
            json = json.replaceAll("[{},:]", "");
            json = json.replaceAll("\"", "\n");
            String array[] = new String[30];
            array = json.split("\n");
            String cidade = array[19], uf = array[23], logradouro = array[7], bairro = array[15];
            //JOptionPane.showMessageDialog(null, "Cidade: " + cidade + "\nUF: " + uf);
            txtCidade.setText(cidade);
            cbEstado.setSelectedItem(uf);
            txtLogradouro.setText(logradouro);
            txtBairro.setText(bairro);
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

